    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(cmbUsuario.getSelectedIndex()==0){
        JOptionPane.showMessageDialog(rootPane, "por favor selecione um cliente");
        cmbUsuario.requestFocusInWindow();
        return;
        }
        int totalquant = Integer.parseInt(txtQuant.getText());
        if(totalquant==0){
            JOptionPane.showMessageDialog(rootPane,"por favor insirir os produtos ");
            cmbProduto.requestFocusInWindow();
            return;
        }
        
        int numFatura = msDados_db.getNumeroFatura();
          msDados_db.adicionarFatura(numFatura,((Opcoes)cmbUsuario.getSelectedItem()).getValor()
                   );
        
       
        
        
        int num = tblDetalhes.getRowCount();
            
            for(int i = 0; i <num; i++){
                msDados_db.adicionarDetalheFatura(
                        numFatura,
                        i,
                        Utilidades.objectToString(tblDetalhes.getValueAt(i, 0)),
                        Utilidades.objectToString(tblDetalhes.getValueAt(i, 1)),
                        Utilidades.objectToInt(tblDetalhes.getValueAt(i, 2)),
                        Utilidades.objectToInt(tblDetalhes.getValueAt(i, 3)));
                
                

                
            }
            
                
        
    JOptionPane.showMessageDialog(rootPane, "venda feita com sucesso");
        
        cmbUsuario.setSelectedIndex(0);
        Limpartabela();
        totais();
        cmbUsuario.requestFocusInWindow();
    }//GEN-LAST:event_btnSalvarActionPerformed

