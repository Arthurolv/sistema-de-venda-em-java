    private void btnAdiconarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdiconarActionPerformed
        if (cmbProduto.getSelectedIndex ()==0) {
    JOptionPane.showMessageDialog(rootPane, "FAVOR, SELECIONAR PRODUTO. !"); 
    cmbProduto.requestFocusInWindow();
    return;
        }

        if (txtQuantidade.getText().equals("")){
           JOptionPane.showMessageDialog(rootPane, "FAVOR DIGITAR UMA QUANTIDADE.!");
         txtQuantidade.requestFocusInWindow();
        return;
        }
        
         if(!Utilidades.isNumeric(txtQuant.getText())){
            JOptionPane.showMessageDialog(rootPane, "por favor inserir um numero numerico");
            txtQuantidade.setText("");
            txtQuantidade.requestFocusInWindow();
            return;
                }
         
        int quantidade = Integer.parseInt(txtQuantidade.getText());
        if(quantidade <= 0){
            JOptionPane.showMessageDialog(rootPane, "por favor inserir um numero acima de 0");
            txtQuantidade.requestFocusInWindow();
            txtQuantidade.setText("");
            return;
                }
        Produto mProduto = msDados_db.getProduto(((Opcoes)cmbProduto.getSelectedItem()).getValor());
        
        String registro[] = new String[5];
        registro[0] = mProduto.getIdProduto();
        registro[1] = mProduto.getDescricao();
        registro[2] = "" + mProduto.getPreco();
        registro[3] = "" + quantidade;
        //registro[4] = "" + (quantidade* mProduto.getPreco()); 
        registro[4] = "" + (mProduto.getPreco() + (mProduto.getPreco() * quantidade / 100.0));
        mTabela.addRow(registro);
        
        cmbProduto.setSelectedIndex(0);
        txtQuantidade.setText("");
        cmbProduto.requestFocusInWindow();
        totais();
           
        
          
         
    }//GEN-LAST:event_btnAdiconarActionPerformed

